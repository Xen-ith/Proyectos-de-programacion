    private void jButtonCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCarritoActionPerformed
        int cantidadAVender = (int) jSpinnerCantidad.getValue();

        if (cantidadAVender == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese una cantidad válida");
        } else {
            try {
                int indice = jTableConsulta.getSelectedRow();
                if (indice == -1) {
                    JOptionPane.showMessageDialog(null, "Seleccione un producto de la tabla");
                    return;
                }

                if (jTextFieldNombreCliente1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Introduzca nombre es obligatorio");
                    return;
                }

                if (jTextFieldCC.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "El campo de cédula del cliente no puede estar vacío");
                    return;
                }

                jLabelNombreClienteP1.setText("<html>"
                        + "<b>Nombre del cliente:</b><br>"
                        + jTextFieldNombreCliente1.getText()
                        + "<br><b>Cedula del cliente:</b><br>"
                        + jTextFieldCC.getText() + "</html>");

                int cantidadDisponible = Integer.parseInt(jTableConsulta.getValueAt(indice, 5).toString()); // Columna de cantidad
                if (cantidadAVender <= cantidadDisponible) {
                    listaVenta.add(new String[]{
                        jTextFieldProducto.getText(),
                        String.valueOf(jSpinnerVenta.getValue()),
                        String.valueOf(jSpinnerCantidad.getValue())
                    });
                    generarTablaVenta();

                    float precio = Float.parseFloat(jTextFieldPrecio.getText());
                    float costeProducto = Float.parseFloat(String.valueOf(jSpinnerVenta.getValue())) * cantidadAVender;
                    float nuevoPrecio = precio + costeProducto;
                    jTextFieldPrecio.setText(String.valueOf(nuevoPrecio));

                    float precioIVA = nuevoPrecio * 0.15f;
                    float precioTOTAL = nuevoPrecio + precioIVA;
                    jTextFieldTotal1.setText(String.valueOf(precioTOTAL));

                } else {
                    JOptionPane.showMessageDialog(null, "Cantidad insuficiente en inventario");
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonCarritoActionPerformed

