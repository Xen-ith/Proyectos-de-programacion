    private void jTableConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultaMouseClicked
        try {
            int indice = jTableConsulta.rowAtPoint(evt.getPoint());
            if (indice >= 0 && indice < jTableConsulta.getRowCount()) {
                jTextFieldProducto.setText(String.valueOf(jTableConsulta.getValueAt(indice, 0)));
                jTextFieldCantidad.setText(String.valueOf(jTableConsulta.getValueAt(indice, 5)));
                jSpinnerVenta.setValue(Integer.valueOf(jTableConsulta.getValueAt(indice, 7).toString()));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar el producto " + e.getMessage());
        }
    }//GEN-LAST:event_jTableConsultaMouseClicked

