    private void ENTERBOTTONMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ENTERBOTTONMouseClicked
        if (UsuarioLogin.getText().equals("admin".toLowerCase())) {
            if (ClaveLogin.getText().equals("123")) {
                MenuPrincipal Principal = new MenuPrincipal();
                Principal.setVentana(Principal);
                Principal.setVisible(true);
                this.setVentana(Principal);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(this, "Datos Incorrectos ", "LOGIN", JOptionPane.INFORMATION_MESSAGE);
                UsuarioLogin.setText("Ingrese su nombre de usuario");
                ClaveLogin.setText("***");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos", "LOGIN", JOptionPane.INFORMATION_MESSAGE);
            UsuarioLogin.setText("Ingrese su nombre de usuario");
            ClaveLogin.setText("***");
        }
    }//GEN-LAST:event_ENTERBOTTONMouseClicked

