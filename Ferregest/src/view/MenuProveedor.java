    private void jLabelEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelEliminarMouseClicked
        try {
            String nombre = jTextFieldnNombreProvedor.getText();
            if (!nombre.isEmpty()) {
                proveedorM.eliminarArchivoProveedor(nombre);
                reiniciarFormulario();
                generarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese su identificacion: ");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jLabelEliminarMouseClicked

    private void jTableProveedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProveedorMouseClicked
        try {
            int indice = jTableProveedor.rowAtPoint(evt.getPoint());
            if (indice >= 0 && indice < jTableProveedor.getRowCount()) {
                jTextFieldnNombreProvedor.setText(String.valueOf(jTableProveedor.getValueAt(indice, 0)));
                jSpinnerIdentificacionProveedor.setValue(Integer.valueOf(jTableProveedor.getValueAt(indice, 1).toString()));
                jSpinnerTelefonoProveedor.setValue(Long.valueOf(jTableProveedor.getValueAt(indice, 2).toString()));
                jTextFieldDirrecionProveedor.setText(String.valueOf(jTableProveedor.getValueAt(indice, 3)));
                jTextFieldEmailProvedor.setText(String.valueOf(jTableProveedor.getValueAt(indice, 4)));
                jComboBoxPagosProveedor.setSelectedItem(String.valueOf(jTableProveedor.getValueAt(indice, 5)));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar el proveedor: " + e.getMessage());
        }
    }//GEN-LAST:event_jTableProveedorMouseClicked

