    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        try {
            String nombre = jTextFieldnNombreProvedor.getText();
            long identificacion = ((Number) jSpinnerIdentificacionProveedor.getValue()).longValue();
            long telefono = ((Number) jSpinnerTelefonoProveedor.getValue()).longValue();
            String direccion = jTextFieldDirrecionProveedor.getText();
            String correo = jTextFieldEmailProvedor.getText();
            String pago = (String) jComboBoxPagosProveedor.getSelectedItem();

            if (proveedorM.validarFormulario(nombre, identificacion, telefono, direccion, correo, pago)) {
                if (proveedorM.existeProveedor(nombre)) {
                    JOptionPane.showMessageDialog(this, "El proveedor " + nombre + " ya existe.");
                } else {
                    proveedorM.proveedor_model.setNombre(nombre);
                    proveedorM.proveedor_model.setIdentificacion(identificacion);
                    proveedorM.proveedor_model.setTelefono(telefono);
                    proveedorM.proveedor_model.setDireccion(direccion);
                    proveedorM.proveedor_model.setCorreo(correo);
                    proveedorM.proveedor_model.setPago(pago);
                    proveedorM.crearArchivoProveedor(proveedorM.proveedor_model, nombre);

                    reiniciarFormulario();
                    generarTabla();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en el formato de los números: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jLabel1MouseClicked

